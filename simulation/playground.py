def standardize_list(lst):
    """Standardizes a list of numbers using z-score normalization."""
    if not lst:
        return []
    
    gamma = 0.94356131553649
    beta = -0.11038455367088

    mean_val = sum(lst) / len(lst)
    std_dev = (sum((x - mean_val) ** 2 for x in lst) / len(lst)) ** 0.5
    
    if std_dev == 0:
        return [0] * len(lst)  # If all values are the same, return 0 for all
    
    return [((x - mean_val) / std_dev)*gamma+beta for x in lst]

